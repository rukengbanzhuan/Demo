package getApiData;

public class Info {
    private int demandTotalNum;
    private int submissionTotalNum;
    private int expertsNum;
    private int activeExpertNum;

    public int getDemandTotalNum() {
        return demandTotalNum;
    }

    public void setDemandTotalNum(int demandTotalNum) {
        this.demandTotalNum = demandTotalNum;
    }

    public int getSubmissionTotalNum() {
        return submissionTotalNum;
    }

    public void setSubmissionTotalNum(int submissionTotalNum) {
        this.submissionTotalNum = submissionTotalNum;
    }

    public int getExpertsNum() {
        return expertsNum;
    }

    public void setExpertsNum(int expertsNum) {
        this.expertsNum = expertsNum;
    }

    public int getActiveExpertNum() {
        return activeExpertNum;
    }

    public void setActiveExpertNum(int activeExpertNum) {
        this.activeExpertNum = activeExpertNum;
    }
}
